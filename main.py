def to_uppercase(input_string):
    return input_string.upper()

# 사용 예제
original_string = "hello, world!"
result = to_uppercase(original_string)
print(result)

def add_numbers(a, b):
    return a + b

# 사용 예제
num1 = 5
num2 = 10
result = add_numbers(num1, num2)
print(f"The sum of {num1} and {num2} is: {result}")
